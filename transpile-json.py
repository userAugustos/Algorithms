def replace_vars(data):
    vars = data.get("vars", {})
    withoutVars = data.get("prod", {})

    for key, value in withoutVars.items():
        if value in vars:
            withoutVars[key] = vars[value]
    
    data["prod"] = withoutVars
    return data
  
